package testsuit.scenario.json;

import testsuit.scenario.ServerConfig;

public class ServerConfigObject implements JSonObject, ServerConfig {

    private String host;
    private int port;

    public void setHost(String host) {
        this.host = host;
    }

    public void setPort(int port) {
        this.port = port;
    }

    @Override
    public String getHost() {
        return host;
    }

    @Override
    public int getPort() {
        return port;
    }
}
